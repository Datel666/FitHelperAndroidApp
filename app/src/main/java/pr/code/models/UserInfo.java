package pr.code.models;

/**
 * This class describes user info model
 */
public class UserInfo {

    String idUserinfo;

    String userAge;

    String userHeight;

    String userWeight;

    String userLifeStyle;

    String userInfoDate;

    String userGender;

    String usergoal;

    public String getIdUserinfo() {
        return idUserinfo;
    }

    public void setIdUserinfo(String idUserinfo) {
        this.idUserinfo = idUserinfo;
    }

    public String getUserAge() {
        return userAge;
    }

    public void setUserAge(String userAge) {
        this.userAge = userAge;
    }

    public String getUserHeight() {
        return userHeight;
    }

    public void setUserHeight(String userHeight) {
        this.userHeight = userHeight;
    }

    public String getUserWeight() {
        return userWeight;
    }

    public void setUserWeight(String userWeight) {
        this.userWeight = userWeight;
    }

    public String getUserLifeStyle() {
        return userLifeStyle;
    }

    public void setUserLifeStyle(String userLifeStyle) {
        this.userLifeStyle = userLifeStyle;
    }

    public String getUserInfoDate() {
        return userInfoDate;
    }

    public void setUserInfoDate(String userInfoDate) {
        this.userInfoDate = userInfoDate;
    }

    public String getUserGender() {
        return userGender;
    }

    public void setUserGender(String userGender) {
        this.userGender = userGender;
    }

    public String getUsergoal() {
        return usergoal;
    }

    public void setUsergoal(String usergoal) {
        this.usergoal = usergoal;
    }
}
